/*
Tacógrafos são dispositivos instalados em determinados tipos de veículos, que registram a velocidade, tempo e distância percorrida por tal veículo. 
É utilizada principalmente em veículos de transporte coletivo e de transporte de cargas, assim ajudando a evitar abusos de velocidade por parte dos motoristas.

A empresa SBC (Sociedade Brasileira dos Caminhoneiros) decidiu encomendar uma versão um pouco mais básica (e barata) para seus associados não precisarem gastar 
tanto na instalação desses aparelhos. Essas versões modificadas registram apenas os intervalos de tempo e as velocidades médias do caminhão naqueles intervalos.

Apesar das restrições dos aparelhos novos, a SBC quer poder saber qual foi a distância percorrida pelos caminhões. Você deverá escrever um programa que recebe 
uma série de intervalos de tempo com suas respectivas velocidades médias e calcula qual foi a distância total percorrida pelo caminhão de acordo com o tacógrafo.

Entrada
A primeira linha da entrada contém um inteiro N (1 =< N <= 1000) representando a quantidade de intervalos de tempo registrados no tacógrafo. As N linhas seguintes 
descrevem os intervalos de tempo. Cada uma dessas linhas possui dois inteiros T e V (1 =< T <= 100, 0 =< V <= 120), que representam, respectivamente o tempo 
decorrido (em horas) e a velocidade média (em quilômetros por hora) no intervalo de tempo.

Saída
Seu programa deve imprimir uma única linha, contendo um único número inteiro representando a distância total percorrida, em quilômetros.
*/

#include <stdio.h>

struct tipo_intervalo{
	int tempo;
	int velocidade_media;
};

typedef struct tipo_intervalo tipo_intervalo;

int main(){
	
	int quantidade, i, total=0;
	tipo_intervalo inter = {0,0};
	
	scanf("%d", &quantidade);
	
	for(i=0; i<quantidade; i++){
		scanf("%d %d", &inter.tempo, &inter.velocidade_media);
		total = total+(inter.tempo*inter.velocidade_media);
	}
	
	printf("%d\n", total);	
	
	return 0;
}
